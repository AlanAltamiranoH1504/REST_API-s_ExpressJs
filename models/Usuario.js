import mongoose from "mongoose";
const {Schema} = mongoose;

const usuarioSchema = new Schema({
    nombre: {
        type: String,
        required: true,
        trim: true
    },
    apellidos: {
        type: String,
        required: true,
        trim: true
    },
    email: {
        type: String,
        required: true,
        trim: true,
        unique: true
    },
    password: {
        type: String,
        required: true,
        trim: true
    }
});

const Usuarios = mongoose.model("Usuarios", usuarioSchema);
export default Usuarios;